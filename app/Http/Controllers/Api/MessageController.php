            'name'=> 'required|min:3',
            'surname'=> 'required|min:2',
            'email'=> 'email:rfc,dns',
            'text'=>'required|max:2000',
            'apartment_id'=> 'exists:apartments,id'
        ]);

        $params['viewed'] = false;
        $message = Message::create($params);

        return response()->json([
            'success' => true,
        ]);
    }

